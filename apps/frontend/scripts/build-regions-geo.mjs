// Generates src/data/br-regions.geo.ts from IBGE's dissolved macro-region (N2) mesh.
//
// Run manually; the output is committed (mirrors br-states.geo.ts / br-municipalities.geo.ts).
// Only 5 dissolved "grande região" polygons (~tens of KB), so unlike the municipality mesh
// this is bundled eagerly and projected at load alongside the states.
//
//   node scripts/build-regions-geo.mjs
//
// Each feature carries `codarea` = the IBGE region code "1".."5" (1 Norte, 2 Nordeste,
// 3 Sudeste, 4 Sul, 5 Centro-Oeste) — the same value as the first digit of a UF code.
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const URL =
  "https://servicodados.ibge.gov.br/api/v3/malhas/paises/BR" +
  "?formato=application/vnd.geo+json&intrarregiao=regiao&qualidade=intermediaria";

const DECIMALS = 3;
const round = (n) => Math.round(n * 10 ** DECIMALS) / 10 ** DECIMALS;

function roundGeometry(geom) {
  const ring = (r) => r.map(([lon, lat]) => [round(lon), round(lat)]);
  if (geom.type === "Polygon") {
    return { type: "Polygon", coordinates: geom.coordinates.map(ring) };
  }
  return { type: "MultiPolygon", coordinates: geom.coordinates.map((poly) => poly.map(ring)) };
}

async function main() {
  console.log("Fetching IBGE macro-region mesh…");
  const res = await fetch(URL, { headers: { Accept: "application/vnd.geo+json" } });
  if (!res.ok) throw new Error(`IBGE returned HTTP ${res.status}`);
  const gj = await res.json();
  const features = gj.features.map((f) => ({
    code: f.properties.codarea,
    geometry: roundGeometry(f.geometry),
  }));
  console.log(`Got ${features.length} macro-regions.`);

  const json = JSON.stringify({ features });
  const banner =
    "// AUTO-GENERATED by scripts/build-regions-geo.mjs — do not edit by hand.\n" +
    "// Real IBGE dissolved macro-region (N2) borders. Bundled (small) and projected at load.\n" +
    "import type { MunicipalitiesGeo } from \"./types\";\n\n";
  const body = `const data = JSON.parse(${JSON.stringify(json)}) as MunicipalitiesGeo;\nexport default data;\n`;

  const out = resolve(dirname(fileURLToPath(import.meta.url)), "../src/data/br-regions.geo.ts");
  writeFileSync(out, banner + body);
  console.log(`Wrote ${out} (${(Buffer.byteLength(banner + body) / 1024).toFixed(0)} KB).`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
